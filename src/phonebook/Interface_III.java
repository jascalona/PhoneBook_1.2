    //Metodo query
    private void query(){
        String busqueda = jBuscar.getText();
        
        DefaultTableModel modelo = new DefaultTableModel(){
            @Override
           public boolean isCellEditable(int row, int column) {
               if (column==0) {
                   return true;
               } else {
                    return false;
               }    
           }
        };
        
        
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Pass");
        modelo.addColumn("Roll");
        
        Tabla.setModel(modelo);
        Tabla.getColumnModel().getColumn(0).setPreferredWidth(20);
        Tabla.getColumnModel().getColumn(1);
        Tabla.getColumnModel().getColumn(2);
        Tabla.getColumnModel().getColumn(3);
        Tabla.getColumnModel().getColumn(4);
            
       String sql = "SELECT *FROM log_phone WHERE NAME LIKE '%"+busqueda+"%' OR SURNAME LIKE '%"+busqueda+"%' OR ROLL LIKE '%"+busqueda+"%' ";
        
        String datos [] = new String [5];
        
        try {
            Conn =  cn.conectar();
            st = Conn.createStatement();
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                datos [0] = rs.getString("id");
                datos [1] = rs.getString("name");
                datos [2] = rs.getString("surname");
                datos [3] = rs.getString("password");
                datos [4] = rs.getString("roll");
                
                modelo.addRow(datos);
            }
            
            Tabla.setModel(modelo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    //Edit
    public void modificar(){
        int rowE =Tabla.getSelectedRow();
            if(rowE>=0){
                EditSU ESU=new EditSU();
                ESU.setVisible(true);
                this.setVisible(false);
                
               //Import datos EditSU 
               ESU.SetModificationid(Tabla.getValueAt(rowE,0).toString());
               ESU.setModification1(Tabla.getValueAt(rowE,1).toString());
               ESU.setModification2(Tabla.getValueAt(rowE,2).toString());
               ESU.setModification3((Tabla.getValueAt(rowE,3).toString()));
               ESU.setModification4((Tabla.getValueAt(rowE,4).toString()));

            } else{
                JOptionPane.showMessageDialog(null, "Seleccione almenos un Registro");
            }
    }
    
    
    //Drop
    public void dropDt(){
        try {
        
            int fila = Tabla.getSelectedRow();
            
            String sql = " DELETE FROM log_phone WHERE id=" +Tabla.getValueAt(fila, 0);
            
            Statement st = Conn.createStatement();
            
            int n=st.executeUpdate(sql);
            
            if (n>=0) {
                JOptionPane.showMessageDialog(null, "Este usario ha sido Eliminado!");
                Interface_III InterIII =new Interface_III();
                InterIII.setVisible(true);
                this.setVisible(false);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ha Ocurrido un Error, debe selecionar almenos un registro!" +e.getMessage());
        }

    }
    
